#!/data/data/com.termux/files/usr/bin/bash
# Katifetch for Android TV - Extended Edition

OS="Android TV (Termux)"
KERNEL=$(uname -r)
HOST=$(hostname)
UPTIME=$(uptime -p | sed 's/up //')
CPU=$(uname -m)
TERMUX_VERSION=$(pkg version | head -n 1 | awk '{print $2}')
MEMINFO=$(free -h | grep Mem:)
USED=$(echo $MEMINFO | awk '{print $3}')
TOTAL=$(echo $MEMINFO | awk '{print $2}')

logo="
\033[1;32m           ===                                    ===
          ****++                                ++****
          #****++                              ++****#
           #*****+                            ++****#
            #*****+           ++++           ++****#
             #****++ ++++===============+++ +*****#
              ##*+++========================+++*##
              ++++============================++++
           +++++===============+++==============+++++
         ++++++=====+++++++++++++++++++++++======++++++
       +++++++++++++++++++++++++++++++++++++++++=++++++++
      ***++++++++++++++++++++++++++++++++++++++++++++++**+
     *****++++@=@@@*++++++++++++++++++++++++%@@@=@++++*****
    ******+++@@@@@@@*++++++++++++++++++++++*@@@@@@@++*******
   **********@@@@+@******++++++++++++++******@#@@@@**********
  **#**********#**********++++++++++++**********#**********#**
 **##********************+++++++++++++********************###**
 *###*******************++++++++++++++++******************####*
 #####*****************++++++++++++++++++****************######
   %%%%%######***********************************#######%%%%%
\033[0m
"

clear
echo -e "$logo"
echo -e "\033[1;36mOS:         \033[0m$OS"
echo -e "\033[1;36mKernel:     \033[0m$KERNEL"
echo -e "\033[1;36mHostname:   \033[0m$HOST"
echo -e "\033[1;36mCPU Arch:   \033[0m$CPU"
echo -e "\033[1;36mUptime:     \033[0m$UPTIME"
echo -e "\033[1;36mMemory:     \033[0m$USED / $TOTAL"
echo -e "\033[1;36mTermux:     \033[0m$TERMUX_VERSION"
